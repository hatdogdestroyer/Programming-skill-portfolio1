# Exercise 7: Some counting

def main():
    # Print a number count from 0 to 50
    print("Count from 1 to 50:")
    for number in range (0, 50,):
        print(number) # Display it to the user
        
 # You have to count 50 to 0 incremnts of -1 so it will display at 0
    print("\nCounting down from 50 to 0:")
    for number in range (50, -1 -1):
        print(number)
        
 # Now count from 30 to 50
    print("\nCounting down from 30 to 50:")
    for number in range (30, 51):
        print(number)
       
# Now you have to count down from 50 to 10 in -2 it goes down by 2 and displays at 10
    print("\nCounting down from 50 to 10 in decremets of 2:")
    for number in range (50, 9, -2):
        print(number)

# Count from 100 to 200 by increments of 5
    print("\nCounting down from 50 to 10 in decrements of 2:")
    for number in range (100, 200, 5):
        print(number) 
main()

      
      
        
      
      
      
      
        
      
    
    
    
    
    
        
        
       
        
       
